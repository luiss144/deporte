    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<HORARIO> liCad = new ArrayList();
        Date d0=(Date)DIA.getDate();
        java.sql.Date ppstime0 = null;
        if(d0 != null){
             ppstime0 = new java.sql.Date(d0.getTime());
        }
        String op1="";
        if(CANCHAJOSETOMASARTETA.isSelected()){

            op1="CANCHA JOSE TOMAS ARTETA(VILLA ESTADIO)";

        }else if (CANCHAPITALDESACO.isSelected()){
            op1="CANCHA PITAL DE SACO";

        }else if(PARQUEANGELALFONSOMOLINA.isSelected()){
            op1="PARQUE ANGEL ALFONSO MOLINA";
        }
        //   String Nombre_prestador, String cedula,String nombrec ,java.sql.Date dia,java.sql.Time incio ,java.sql.Time fin,String estado
        if(nom_lib.getText().length()==0&&op1==""&&cedula.getText().length()==0&&DIA.getDate().hashCode()==0){
            JOptionPane.showMessageDialog(null,"datos no ingresado");

        }else if(nom_lib.getText().length()!=0&&op1!=""&&cedula.getText().length()!=0&&DIA.getDate().hashCode()!=0) {
            liCad=li.Buscarhorario(nom_lib.getText(),cedula.getText(),op1,ppstime0);

        } else if(op1!=""&&nom_lib.getText().length()==0&&cedula.getText().length()==0){

            liCad=li.Buscarhorariocancha(op1);

        }else if(cedula.getText().length()!=0){
            liCad=li.Buscarprestadorcedula(cedula.getText());
        }else if(nom_lib.getText().length()!=0){
            liCad=li.Buscarhorarionombre(nom_lib.getText());
        }else if(DIA.getDate().hashCode()!=0){
            liCad=li.Buscarhorariofecha(ppstime0);
        }
        int t=liCad.size();
        String td=""+t;
        total.setText(td);
        String nombreColumana[]={"ID HORARIO","CEDULA ","NOMBRE PRESTADOR","TELEFONO","CANCHA","DIA","INICIO","HASTA","ESTADO" };
        String data[][]=new String [t][9];
        for(int i=0;i<t;i++){
            data[i][0]=liCad.get(i).getId_horario();
            data[i][1]=liCad.get(i).getCedula_prestador();
            data[i][2]=liCad.get(i).getNombre_prestador();
            data[i][3]=liCad.get(i).getTelefono_prestador();
            data[i][4]=liCad.get(i).getNombre_cancha();
            data[i][5]=""+liCad.get(i).getDia_prestamo();
            data[i][6]=""+liCad.get(i).getInicio_horario();
            data[i][7]=""+liCad.get(i).getFinal_horario();
            data[i][8]=liCad.get(i).getEstado();

        }
        LISTA.setModel(new javax.swing.table.DefaultTableModel(data,nombreColumana));
        nom_lib.setText(null);
        cedula.setText(null);

        buttonGroup1.clearSelection();
        buttonGroup2.clearSelection();
    }//GEN-LAST:event_jButton1ActionPerformed

